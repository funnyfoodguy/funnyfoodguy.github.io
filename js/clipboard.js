const copyBtn1 = document.querySelector('.copy-btn1');
const text1 = document.querySelector('.text1');
function copy1() {
    navigator.clipboard.writeText(text1.innerText);

    copyBtn1.innerText = 'Copied';
    text1.classList.add('copied');

    setTimeout(() => {
        text1.classList.remove('copied');
        copyBtn1.innerText = 'Copy';
    }, 1000);
}

